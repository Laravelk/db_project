package Data;

public class StatementData {
    public StatementData(){}

    private int count;
    private int real_wrap;
    private int cost_insurance;
    private int weight;
    private int volume;

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getReal_wrap() {
        return real_wrap;
    }

    public void setReal_wrap(int real_wrap) {
        this.real_wrap = real_wrap;
    }

    public int getCost_insurance() {
        return cost_insurance;
    }

    public void setCost_insurance(int cost_insurance) {
        this.cost_insurance = cost_insurance;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getVolume() {
        return volume;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }
}
